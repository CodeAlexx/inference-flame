//! Integration tests for the GGUF loader.
//!
//! These tests exercise the pure-CPU path: binary parse, dequant math,
//! key remap. They do NOT require a CUDA device and run on any host.
//!
//! The `load_real_file` / `load_real_file_shapes` tests are `#[ignore]` by
//! default because they require an actual GGUF file on disk and a working
//! CUDA device. Enable with:
//!
//! ```bash
//! GGUF_TEST_FILE=/path/to/some.gguf \
//!     cargo test --release -- --ignored
//! ```
//!
//! Most dequant tests here use **reference-validated** expected values: the
//! byte arrays and the expected f32 outputs were generated by running
//! llama.cpp's canonical `dequantize_row_q{8_0,4_K,5_K,6_K}` kernels (see
//! `tools/gguf_ref/gen_ref.c`). Earlier versions of these tests derived
//! expected values from the same formula the Rust code implements, which
//! made them self-consistency tautologies rather than correctness proofs.
//! The hand-picked block contents exercise the full state space that the
//! previous "one scale nonzero" tests missed:
//!   - Q4_K: all 8 sub-blocks have distinct non-zero scales AND mins,
//!     exercising both the j<4 AND j>=4 branches of `get_scale_min_k4`
//!     and a non-zero dmin.
//!   - Q5_K: one case covers all 4 qh shift positions (u1/u2 <<= 2
//!     progression); a second case uses qh = 0xAA to alternate hit/miss
//!     and thus validates each individual shift step.
//!   - Q6_K: qh = 0xE4 (0b11100100) extracts 0,1,2,3 at shifts 0,2,4,6
//!     respectively, validating all four 2-bit positions of the qh byte.

use inference_flame::gguf::dequant::dequantize_to_f32;
use inference_flame::gguf::reader::GgufQuantType;
use inference_flame::gguf::{default_rename, read_header};

/// Build a tiny synthetic GGUF blob (version 3, 0 tensors, 1 metadata
/// key) and parse it back via the public `parse_header` path.
#[test]
fn header_parse_synthetic() {
    let mut buf: Vec<u8> = Vec::new();
    buf.extend_from_slice(b"GGUF");
    buf.extend_from_slice(&3u32.to_le_bytes()); // version
    buf.extend_from_slice(&0u64.to_le_bytes()); // n_tensors
    buf.extend_from_slice(&1u64.to_le_bytes()); // n_metadata

    let key = b"general.architecture";
    buf.extend_from_slice(&(key.len() as u64).to_le_bytes());
    buf.extend_from_slice(key);
    buf.extend_from_slice(&8u32.to_le_bytes()); // TYPE_STRING
    let val = b"flux";
    buf.extend_from_slice(&(val.len() as u64).to_le_bytes());
    buf.extend_from_slice(val);

    let tmp_dir = std::env::temp_dir();
    let tmp = tmp_dir.join(format!("gguf_test_synth_{}.gguf", std::process::id()));
    std::fs::write(&tmp, &buf).expect("write tmp");
    let header = read_header(&tmp).expect("parse");
    std::fs::remove_file(&tmp).ok();

    assert_eq!(header.version, 3);
    assert_eq!(header.tensors.len(), 0);
    assert_eq!(header.architecture.as_deref(), Some("flux"));
}

