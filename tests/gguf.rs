fn put_f16(out: &mut Vec<u8>, v: f32) {
    let bits = half::f16::from_f32(v).to_bits();
    out.extend_from_slice(&bits.to_le_bytes());
}

/// Q8_0: reference-validated. d=2.0, qs = -16..15 → y = 2*qs.
#[test]
fn dequant_q8_0_reference() {
    let out = dequantize_to_f32(GgufQuantType::Q8_0, &Q8_0_BLOCK_BASIC, 32).unwrap();
    assert_eq!(out.len(), 32);
    for (i, (&got, &expected)) in out.iter().zip(Q8_0_REF_BASIC.iter()).enumerate() {
        assert!(
            (got - expected).abs() < 1e-4,
            "i={i} got={got} expected={expected}"
        );
    }
}

/// Q4_K: reference-validated across all 8 sub-blocks with distinct non-zero
/// scales AND mins. Exercises `get_scale_min_k4` for j<4 AND j>=4.
#[test]
fn dequant_q4_k_reference_distinct_scales() {
    let out = dequantize_to_f32(GgufQuantType::Q4_K, &Q4_K_BLOCK_DISTINCT_SCALES, 256).unwrap();
    assert_eq!(out.len(), 256);
    for group in 0..8 {
        let expected = Q4_K_REF_DISTINCT_SCALES_GROUPS[group];
        for l in 0..32 {
            let i = group * 32 + l;
            assert!(
                (out[i] - expected).abs() < 1e-3,
                "group={group} i={i} got={} expected={}",
                out[i],
                expected
            );
        }
    }
}

/// Q5_K: qh = 0xFF → every bit set → every element's high bit contributes
/// 16 → output uniform at 16.0. Covers all 4 (u1, u2) shift pairs.
#[test]
fn dequant_q5_k_reference_all_high_bits() {
    let out = dequantize_to_f32(GgufQuantType::Q5_K, &Q5_K_BLOCK_ALL_HIGH_BITS, 256).unwrap();
    assert_eq!(out.len(), 256);
    for (i, &v) in out.iter().enumerate() {
        assert!((v - 16.0).abs() < 1e-4, "i={i} got={v} expected 16.0");
    }
}

/// Q5_K: qh = 0xAA → alternating bit pattern.
/// This validates the u1,u2 <<= 2 shift progression directly.
/// Lo-nibble sub-blocks (0,2,4,6) get u1 hitting a '0' → y=0.
/// Hi-nibble sub-blocks (1,3,5,7) get u2 hitting a '1' → y=16.
#[test]
fn dequant_q5_k_reference_alternating_qh() {
    let out = dequantize_to_f32(GgufQuantType::Q5_K, &Q5_K_BLOCK_ALTERNATING_QH, 256).unwrap();
    assert_eq!(out.len(), 256);
    for sub in 0..8 {
        let expected = if sub % 2 == 0 { 0.0 } else { 16.0 };
        for l in 0..32 {
            let i = sub * 32 + l;
            assert!(
                (out[i] - expected).abs() < 1e-4,
                "sub={sub} i={i} got={} expected={}",
                out[i],
                expected
            );
        }
    }
}

/// Q6_K: qh byte 0xE4 contains the four 2-bit values {0, 1, 2, 3} at shifts
/// {0, 2, 4, 6}. With ql=0 and scales=1 the four 32-element groups per half
/// must be [-32, -16, 0, 16]. This validates all four 2-bit qh extractions.
#[test]
fn dequant_q6_k_reference_four_shifts() {
    let out = dequantize_to_f32(GgufQuantType::Q6_K, &Q6_K_BLOCK_FOUR_SHIFTS, 256).unwrap();
    assert_eq!(out.len(), 256);

    // First 128-element half: four 32-elem groups at shifts 0,2,4,6.
    let groups = [-32.0f32, -16.0, 0.0, 16.0];
    for (g, &expected) in groups.iter().enumerate() {
        for l in 0..32 {
            let i = g * 32 + l;
            assert!(
                (out[i] - expected).abs() < 1e-4,
                "half 0 g={g} i={i} got={} expected={}",
                out[i],
                expected
            );
        }
    }
    // Second 128-element half: same pattern (scales[8..16] also all 1).
    for (g, &expected) in groups.iter().enumerate() {
        for l in 0..32 {
            let i = 128 + g * 32 + l;
            assert!(
                (out[i] - expected).abs() < 1e-4,
                "half 1 g={g} i={i} got={} expected={}",
                out[i],
                expected
            );
        }
    }
}

/// Trivial F16 and BF16 passthroughs via the dispatcher.
#[test]
fn f16_bf16_passthrough() {
    let mut bytes = Vec::new();
    for v in [0.0f32, 1.0, -1.0, 2.5, -3.75] {
        put_f16(&mut bytes, v);
    }
    let out = dequantize_to_f32(GgufQuantType::F16, &bytes, 5).unwrap();
    for (got, expected) in out.iter().zip([0.0, 1.0, -1.0, 2.5, -3.75].iter()) {
        assert!((got - expected).abs() < 1e-3);
    }

    let mut bytes = Vec::new();
    for v in [0.0f32, 1.0, -1.0, 2.5, -3.75] {
        let bits = half::bf16::from_f32(v).to_bits();
        bytes.extend_from_slice(&bits.to_le_bytes());
    }
    let out = dequantize_to_f32(GgufQuantType::BF16, &bytes, 5).unwrap();
    for (got, expected) in out.iter().zip([0.0, 1.0, -1.0, 2.5, -3.75].iter()) {
        assert!((got - expected).abs() < 1e-2);
    }
}

