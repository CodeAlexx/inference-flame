/// Minimal valid GGUF (version 3) with a single F32 tensor of
/// **GGUF-order** shape [3, 5, 7]. In GGUF convention that's
/// "fastest-moving-first"; our reader must expose this as Rust-side
/// dims = [7, 5, 3] (Pytorch-order).
///
/// Total elements: 3 * 5 * 7 = 105.
/// Byte size: 105 * 4 = 420.
#[test]
fn dim_reversal_from_synthetic_gguf() {
    use inference_flame::gguf::reader::{parse_header, GgufQuantType};

    let mut buf: Vec<u8> = Vec::new();
    // Magic + version.
    buf.extend_from_slice(b"GGUF");
    buf.extend_from_slice(&3u32.to_le_bytes());
    // n_tensors = 1, n_metadata = 0.
    buf.extend_from_slice(&1u64.to_le_bytes());
    buf.extend_from_slice(&0u64.to_le_bytes());

    // Tensor info.
    // - name "t" (length 1 + utf8 byte).
    // - n_dims = 3.
    // - dims = [3, 5, 7] (GGUF storage order, fastest-first).
    // - type = 0 (F32).
    // - data_offset_rel = 0.
    let name = b"t";
    buf.extend_from_slice(&(name.len() as u64).to_le_bytes());
    buf.extend_from_slice(name);
    buf.extend_from_slice(&3u32.to_le_bytes()); // n_dims
    buf.extend_from_slice(&3u64.to_le_bytes()); // dim[0]
    buf.extend_from_slice(&5u64.to_le_bytes()); // dim[1]
    buf.extend_from_slice(&7u64.to_le_bytes()); // dim[2]
    buf.extend_from_slice(&0u32.to_le_bytes()); // ggml_type F32
    buf.extend_from_slice(&0u64.to_le_bytes()); // data_offset_rel

    // Pad to alignment 32, then 420 bytes of zeroed payload.
    while buf.len() % 32 != 0 {
        buf.push(0);
    }
    buf.extend_from_slice(&vec![0u8; 420]);

    let header = parse_header(&buf).expect("parse synthetic GGUF");
    assert_eq!(header.tensors.len(), 1);
    let info = &header.tensors[0];
    assert_eq!(info.name, "t");
    assert_eq!(info.quant, GgufQuantType::F32);

    // The core assertion: dims MUST be reversed from the GGUF storage order.
    // GGUF wrote [3, 5, 7], Rust side must see [7, 5, 3].
    assert_eq!(
        info.dims,
        vec![7usize, 5, 3],
        "dims must be reversed from GGUF storage order"
    );
    assert_eq!(info.n_elements(), 3 * 5 * 7);
}

/// Synthetic GGUF 2D case: GGUF [IN=4, OUT=6] → Rust [OUT=6, IN=4]
/// (this is the linear-weight convention that the rest of flame-core
/// consumes).
#[test]
fn dim_reversal_linear_weight_convention() {
    use inference_flame::gguf::reader::parse_header;

    let mut buf: Vec<u8> = Vec::new();
    buf.extend_from_slice(b"GGUF");
    buf.extend_from_slice(&3u32.to_le_bytes());
    buf.extend_from_slice(&1u64.to_le_bytes()); // n_tensors
    buf.extend_from_slice(&0u64.to_le_bytes()); // n_metadata

    let name = b"linear.weight";
    buf.extend_from_slice(&(name.len() as u64).to_le_bytes());
    buf.extend_from_slice(name);
    buf.extend_from_slice(&2u32.to_le_bytes()); // n_dims
    buf.extend_from_slice(&4u64.to_le_bytes()); // GGUF dim[0] = IN
    buf.extend_from_slice(&6u64.to_le_bytes()); // GGUF dim[1] = OUT
    buf.extend_from_slice(&0u32.to_le_bytes()); // F32
    buf.extend_from_slice(&0u64.to_le_bytes()); // offset rel

    while buf.len() % 32 != 0 {
        buf.push(0);
    }
    buf.extend_from_slice(&vec![0u8; 4 * 6 * 4]); // 24 f32s

    let header = parse_header(&buf).expect("parse");
    let info = &header.tensors[0];
    assert_eq!(
        info.dims,
        vec![6usize, 4],
        "linear weight must be [OUT, IN] after reversal"
    );
}

/// End-to-end load test. Ignored by default — run with
/// `GGUF_TEST_FILE=/path/to/some.gguf cargo test --release -- --ignored`.
/// Requires a working CUDA device.
#[test]
#[ignore]
fn load_real_file() {
    use std::path::Path;
    let path = match std::env::var("GGUF_TEST_FILE") {
        Ok(p) => p,
        Err(_) => {
            eprintln!("GGUF_TEST_FILE not set; skipping load_real_file");
            return;
        }
    };

    let header = read_header(Path::new(&path)).expect("read_header");
    eprintln!(
        "GGUF {} v{} arch={:?} tensors={} alignment={}",
        path,
        header.version,
        header.architecture,
        header.tensors.len(),
        header.alignment
    );
    assert!(!header.tensors.is_empty());

    let device = match flame_core::CudaDevice::new(0) {
        Ok(d) => d,
        Err(e) => {
            eprintln!("no CUDA device available ({e:?}); skipping full load");
            return;
        }
    };

    let tensors = inference_flame::gguf::load_file_gguf(Path::new(&path), device)
        .expect("load_file_gguf");
    assert_eq!(tensors.len(), header.tensors.len());
}

/// Shape verification against a known-good GGUF. Ignored; enable with
/// ```bash
/// GGUF_TEST_FILE=/path/to/some.gguf \
/// GGUF_TEST_EXPECTED_SHAPES="key1:dim1,dim2;key2:dim1,dim2,dim3" \
/// cargo test --release -- --ignored load_real_file_shapes
/// ```
/// This is how the dim-reversal fix (P1-3) gets its real-world check
/// in CI: feed a known checkpoint + the shapes of one or two tensors
/// pulled from the Python `gguf` library / safetensors reference.
#[test]
#[ignore]
fn load_real_file_shapes() {
    use std::path::Path;
    let path = match std::env::var("GGUF_TEST_FILE") {
        Ok(p) => p,
        Err(_) => {
            eprintln!("GGUF_TEST_FILE not set; skipping load_real_file_shapes");
            return;
        }
    };
    let expected_shapes = match std::env::var("GGUF_TEST_EXPECTED_SHAPES") {
        Ok(s) => s,
        Err(_) => {
            eprintln!(
                "GGUF_TEST_EXPECTED_SHAPES not set; skipping (format: \
                 \"key:d1,d2;key2:d1,d2,d3\")"
            );
            return;
        }
    };

    let device = match flame_core::CudaDevice::new(0) {
        Ok(d) => d,
        Err(e) => {
            eprintln!("no CUDA device available ({e:?}); cannot run shape check");
            return;
        }
    };
    let weights = inference_flame::gguf::load_file_gguf(Path::new(&path), device)
        .expect("load_file_gguf");

    for entry in expected_shapes.split(';').filter(|s| !s.is_empty()) {
        let (key, dims_str) = entry
            .split_once(':')
            .unwrap_or_else(|| panic!("bad GGUF_TEST_EXPECTED_SHAPES entry: {entry}"));
        let expected: Vec<usize> = dims_str
            .split(',')
            .map(|s| s.parse().expect("parse dim"))
            .collect();
        let t = weights
            .get(key)
            .unwrap_or_else(|| panic!("key {key} missing from loaded weights"));
        assert_eq!(
            t.shape().dims(),
            expected.as_slice(),
            "shape mismatch for key {key}: got {:?}, expected {:?}",
            t.shape().dims(),
            expected
        );
    }
}
