// ---------------------------------------------------------------------------
// Dim-reversal validation (P1-3 in SKEPTIC_GGUF.md).
//
// The reader reverses GGUF dims on parse so that `dims` matches PyTorch's
// `[out, in, ...]` convention. A wrong-order-but-same-product tensor would
// load silently and multiply without error — producing garbage outputs that
// are impossible to diagnose from a stack trace. This is the highest-risk
// silent failure in the loader.
//
// We cover it in two ways:
//   1. A synthetic GGUF built entirely in memory whose ONE F32 tensor has
//      a known non-square 3D shape. We assert the post-parse Rust-side
//      dims are the exact REVERSE of the dims we wrote. No external file,
//      runs on any host (no CUDA).
//   2. An ignored real-file test keyed on GGUF_TEST_FILE /
//      GGUF_TEST_EXPECTED_SHAPES env vars for when a user wants to check
//      a specific checkpoint's shape registry.
